class Solution {
  public:
    int firstElement(int n) {
        int a=0,b=1,t=1,mod=1000000007;
        for (int i=2;i<=n;i++){
            t=(a+b)%mod;
            a=b;
            b=t;
        }
        return t;
    }
};

